//配列から一つの位置を選択

function selectRandomElement(arr) {
    if (arr.length === 0) {
        return 'なし'; // 空配列はスキップ
    }

    const randomIndex = Math.floor(Math.random() * arr.length);
    return arr[randomIndex];
}

