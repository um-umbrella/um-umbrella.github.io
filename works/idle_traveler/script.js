//状況設定

//街中なら１，野外なら２
let nowPlaying = [1, 6];

function AreaChange() {
    console.log(`　エリア移動：${nowPlaying[0]}から～`);

    if (Math.floor(Math.random() * 100) < 40) {
        nowPlaying[0] = 1;
    } else {
        nowPlaying[0] = 2;
    }

    console.log(`　　${nowPlaying[0]}へ`);
}

function Walk() {
    //読み込むごとに経過を減らす
    nowPlaying[1] -= 1;

    if (nowPlaying[1] <= 0) {
        AreaChange();
        if (nowPlaying[0] === 1) {
            TextEdit(`　　街に入った。`);
        } else {
            TextEdit(`　　野外を移動する。`);
        }

        nowPlaying[1] = Math.floor(Math.random() * 24 + 6);
    }
}

