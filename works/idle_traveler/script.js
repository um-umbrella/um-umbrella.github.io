//時刻を設定

const timeManager = {
    currentHour: Math.floor(Math.random() * 24), // ランダムな開始時刻
    previousHour: null, // 直前の時刻を保存
    dayCount: 1, // 経過日数

    advanceTime(hours) {
        this.previousHour = this.currentHour; // 直前の時刻を保存
        this.currentHour = (this.currentHour + hours) % 24;

        // 🌟 日付が変わった判定（23時から0時をまたいだ場合）
        if (this.previousHour > this.currentHour) {
            this.dayCount++; // 日付を1つ進める
            this.onNewDay(); // 日付が変わったイベントを実行
        }

        // 🌙 夜のイベント（20時～6時）
        if (this.currentHour >= 20 || this.currentHour < 6) {
            console.log('◇夜');
        }

        console.log(`◇現在時刻: ${this.currentHour}時（${this.dayCount}日目）`);
    },

    onNewDay() {
        console.log(`■新しい日！（${this.dayCount}日目）`);
        // ここに日付が変わったときの処理
        TextEdit('　　一日が終わった。');
    },
};

