//改行調整

function normalizeNewlines(text) {
    return text
        .replace(/\r\n|\r|\n/g, '\n') // 改行を\nに統一
        .replace(/\n{2,}/g, '\n'); // 2つ以上の連続した改行を\nに統一
}
