//引数の内容を書き足す
function TextEdit(text) {
    const logContainer = document.getElementById('logContainer');
    const logEntry = document.createElement('p');

    logEntry.textContent = text;
    logContainer.appendChild(logEntry);
    logContainer.scrollTop = logContainer.scrollHeight; // 自動スクロール
}

function addLog() {
    //時間を進める
    timeManager.advanceTime(Math.floor(Math.random() * 6) + 4);
    console.log('　現在地（１なら街中）：' + nowPlaying[0]);
    let area;
    if (nowPlaying[0] === 1) {
        //現在地：街中
        area = logsIndoor[Math.floor(Math.random() * logsIndoor.length)];
    } else {
        //現在地：野外
        area = logsOutdoor[Math.floor(Math.random() * logsOutdoor.length)];
    }

    let text = `${timeManager.dayCount}日目 ${timeManager.currentHour}時　｜　` + area;

    TextEdit(text);

    Walk();
}

